#Data types in python
def main():
    no=10
    print("Given value is:",no)
    print("Data type of no:",type(no))
    print("")

    no=10.25
    print("Given value is:",no)
    print("Data type of no:",type(no))
    print("")

    no="Shraddha"
    print("Given value is:",no)
    print("Data type of no:",type(no))
    print("")
    
    no=True
    print("Given value is:",no)
    print("Data type of no:",type(no))
    print("")

    no=3 + 7j
    print("Given value is:",no)
    print("Data type of no:",type(no))

if __name__=="__main__":
    main()